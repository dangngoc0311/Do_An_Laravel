    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $payment = Payment::orderBy('created_at', 'DESC')->search()->paginate(5);
        if (isset($_GET['sort_by'])) {
            $sortBy = $_GET['sort_by'];
            $pieces = explode("_", $sortBy);
            $payment = Payment::orderBy($pieces[0], $pieces[1])->search()->paginate(5);
        }
        if (isset($_GET['status'])) {
            $sortBy = $_GET['status'];
            $payment = Payment::where('status', $sortBy)->search()->paginate(5);
        }
        return view('backend.pages.payment.list_payment', compact('payment'));
    }

